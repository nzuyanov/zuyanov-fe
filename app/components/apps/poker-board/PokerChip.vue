<script setup lang="ts">
const props = withDefaults(defineProps<{
	color?: string,
	secondaryColor?: string,
	size?: number,
	value?: number | string,
}>(), {
	color: '#2563EB',
	secondaryColor: '#FFFCED',
	size: 24,
	value: '',
})

const DARKNESS_FACTOR = 0.3

const darkenHexColor = (hexColor: string) => {
	// Убираем символ #, если есть
	hexColor = hexColor.replace('#', '')

	// Преобразуем HEX в RGB
	const r = parseInt(hexColor.substring(0, 2), 16)
	const g = parseInt(hexColor.substring(2, 4), 16)
	const b = parseInt(hexColor.substring(4, 6), 16)

	// Уменьшаем яркость каждого канала на заданный коэффициент (0.0–1.0)
	const darkR = Math.max(0, Math.floor(r * (1 - DARKNESS_FACTOR)))
	const darkG = Math.max(0, Math.floor(g * (1 - DARKNESS_FACTOR)))
	const darkB = Math.max(0, Math.floor(b * (1 - DARKNESS_FACTOR)))

	// Преобразуем обратно в HEX
	const toHex = (value: number) => value.toString(16).padStart(2, '0')

	return `#${toHex(darkR)}${toHex(darkG)}${toHex(darkB)}`
}

const darkenRgbColor = (rgbString: string) => {
	// Извлекаем значения R, G, B из строки 'rgb(255, 87, 51)'
	const match = rgbString.match(/rgb\((\d+),\s*(\d+),\s*(\d+)\)/);
	if (!match) return ''

	const [, rStr, gStr, bStr] = match

	if (!rStr || !gStr || !bStr) return ''

	const r = parseInt(rStr, 10)
	const g = parseInt(gStr, 10)
	const b = parseInt(bStr, 10)

	// Уменьшаем яркость
	const darkR = Math.max(0, Math.floor(r * (1 - DARKNESS_FACTOR)))
	const darkG = Math.max(0, Math.floor(g * (1 - DARKNESS_FACTOR)))
	const darkB = Math.max(0, Math.floor(b * (1 - DARKNESS_FACTOR)))

	return `rgb(${darkR}, ${darkG}, ${darkB})`
}

const darkenColor = (color: string) => {
	if (color.startsWith('#')) {
		return darkenHexColor(color);
	} else if (color.startsWith('rgb')) {
		return darkenRgbColor(color);
	} else {
		console.error('Поддерживаются только HEX (#RRGGBB) и RGB (rgb(R, G, B)) форматы')
		return ''
	}
}

const colorFaded = computed<string>(() => darkenColor(props.color))
const secondaryColorFaded = computed<string>(() => darkenColor(props.secondaryColor))
const placeholderColor = computed<string>(() => {
	if (!!props.value) return props.color
	return props.secondaryColor
})

